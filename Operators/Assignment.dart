void main() {
  var A = 10;
  var B = 7;

  print("Before compound assignment operator: ");
  print(A);

  A += B;

  print("After using compound assignment: ");
  print(A);

  //&=
  A = 10;
  B = 3;

  print("Before compound assignment operators: ");
  print(A);

  A &= B;

  print("After using compound assignment operator: ");
  print(A);
}
